#include <unistd.h>
#include<string.h>

int     ft_is_numeric(char *str)
{
    int     i;
    i = 0;
    
    while(str[i])
    {
        if(str[i] >= '0' && str[i] <= '9')
            return 1;
        else
            return 0;
        
    }
}
