#include<unistd.h>
#include<string.h>

char 		*ft_strcpy(char *dest, char *arc)
{
	int		i;
	i = 0;
	while(arc[i] != '\0')
	{
		dest[i] = arc[i];
		i++;
	}
	return dest;
}
